#!/usr/bin/env python3
"""
Concierge interactive simulator (terminal).

Two modes:

  Interactive (default) — type partner messages, see concierge replies inline,
  save the session to an xlsx whenever you want.

  Validation (--validate) — replay every fixture in
  tests/fixtures/concierge_replay.yaml, generate the next concierge reply with
  the new prompt, and dump everything to xlsx in the production conversation
  schema (one sheet, conversation-level metadata only on the first row of each
  conversation, message_window.* fields per row).

Output xlsx schema (matches the production conversation export):
  conversation_id, partner_id, first_name, last_name, zone_description,
  phone_number, start_date, started_at, last_message_at, total_message_count,
  status, partner_msg_count, agent_msg_count,
  message_window.confidence, message_window.content, message_window.created_at,
  message_window.intent_classified, message_window.llm_model,
  message_window.message_id, message_window.role,
  message_window.tokens_in, message_window.tokens_out

Usage:
  python3 scripts/simulate_concierge.py
  python3 scripts/simulate_concierge.py --name Walter
  python3 scripts/simulate_concierge.py --validate
  python3 scripts/simulate_concierge.py --validate --out tests/simulations/spec-validation.xlsx

Interactive commands at the partner-message prompt:
  :name <first_name>     set/change the partner's first name
  :system <text>         seed a concierge message into history (no LLM call)
  :reset [label]         finish current run, start a new one
  :save [path.xlsx]      write all runs in this session to xlsx
  :show                  print current run transcript
  :runs                  list all runs in this session
  :prompt                dump the full prompt that would be sent next turn
  :model <name>          haiku | sonnet | opus
  :help                  show this help
  :quit / :q             exit (auto-saves if --out provided)
"""

