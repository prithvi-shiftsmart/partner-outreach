DEFAULT_MODEL = "haiku"
TIMEOUT_SEC = 120
SIMULATIONS_DIR = WORKSPACE / "tests" / "simulations"
FIXTURES_PATH = WORKSPACE / "tests" / "fixtures" / "concierge_replay.yaml"

# Production model used for original conversation history (gemini-2.5-flash).
# New replies generated by this simulator are tagged with the local replay
# model identifier so reviewers can tell them apart in the xlsx.
PROD_MODEL_TAG = "gemini-2.5-flash"
LOCAL_MODEL_TAG = {
    "haiku": "claude-haiku-4-5-20251001",
    "sonnet": "claude-sonnet-4-6",
    "opus": "claude-opus-4-7",
}


def now_ts() -> str:
    return dt.datetime.now().strftime("%Y%m%d-%H%M%S")


def fmt_dt(d: datetime) -> str:
    """Match production export format like '4/29/2026 13:46:10'."""
    return f"{d.month}/{d.day}/{d.year} {d.hour}:{d.minute:02d}:{d.second:02d}"


async def run_claude(prompt: str, model: str) -> tuple[str, str, int]:
    proc = await asyncio.create_subprocess_exec(
        CLAUDE_CLI_PATH, "-p", "--model", model,
        stdin=asyncio.subprocess.PIPE,
        stdout=asyncio.subprocess.PIPE,
        stderr=asyncio.subprocess.PIPE,
    )
    try:
        stdout, stderr = await asyncio.wait_for(
            proc.communicate(input=prompt.encode()),
            timeout=TIMEOUT_SEC,
        )
    except asyncio.TimeoutError:
        proc.kill()
        return "", "TIMEOUT", -1
    return stdout.decode("utf-8", errors="replace"), stderr.decode("utf-8", errors="replace"), proc.returncode


def clean_output(raw: str) -> str:
    s = raw.strip()
    if len(s) >= 2 and s[0] in ('"', "'", "`") and s[-1] == s[0]:
        s = s[1:-1].strip()
    if s.startswith("```"):
        lines = s.split("\n")[1:]
        if lines and lines[-1].strip() == "```":
            lines = lines[:-1]
        s = "\n".join(lines).strip()
    return s


def to_messages(history: list[dict]) -> list[dict]:
    return [
        {"direction": "inbound" if h["role"] == "partner" else "outbound",
         "content": h["content"],
         "ts": h["ts"]}
        for h in history
    ]


